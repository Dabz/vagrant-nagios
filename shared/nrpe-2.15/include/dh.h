#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xBA,0x35,0x97,0x33,0x0D,0x37,0xCA,0xC4,0x88,0xEE,0xD9,0xAA,
		0xBC,0x27,0x41,0x77,0x23,0x91,0xA2,0xDE,0xAF,0x90,0xBA,0x76,
		0xF0,0x25,0x2F,0x9F,0xD7,0x29,0x42,0xAD,0x2F,0x1C,0xD4,0x71,
		0x23,0x43,0x14,0x53,0x78,0xEB,0xDE,0x38,0x9A,0x9F,0xF7,0xB5,
		0x82,0x46,0x78,0xBA,0xCD,0xE8,0x04,0x47,0x6F,0xC6,0x46,0x72,
		0xF7,0xC4,0xF1,0x83,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
